import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const dataPath = path.join(__dirname, "wecom-font-styles-data.json");
const outPath = path.join(__dirname, "plugin-wecom-fonts", "code.js");

const data = JSON.parse(fs.readFileSync(dataPath, "utf8"));

const template = `// Auto-generated by build-wecom-fonts-plugin.mjs — do not edit by hand.
const DATA = __DATA__;

function styleName(platform, display) {
  return "WeCom/" + platform + "/" + display;
}

async function loadFont(family, style) {
  await figma.loadFontAsync({ family, style });
}

async function loadAllFonts(entries) {
  const seen = new Set();
  const errors = [];
  for (const row of entries) {
    for (const p of row.platforms) {
      const key = p.family + "|" + p.style;
      if (seen.has(key)) continue;
      seen.add(key);
      try {
        await loadFont(p.family, p.style);
      } catch (e) {
        errors.push({ family: p.family, style: p.style, error: String(e) });
      }
    }
  }
  return errors;
}

function flattenEntries() {
  const out = [];
  for (const row of DATA.desktop) {
    for (const p of row.platforms) {
      out.push({ platform: p.platform, display: row.display, slug: row.slug, size: row.size, lineHeightPx: row.lineHeightPx, family: p.family, style: p.style, weight: p.weight });
    }
  }
  for (const row of DATA.mobile) {
    for (const p of row.platforms) {
      out.push({ platform: p.platform, display: row.display, slug: row.slug, size: row.size, lineHeightPx: row.lineHeightPx, family: p.family, style: p.style, weight: p.weight });
    }
  }
  return out;
}

async function createStyles(entries) {
  const created = [];
  const existing = await figma.getLocalTextStylesAsync();
  const byName = {};
  for (let i = 0; i < existing.length; i++) {
    byName[existing[i].name] = existing[i];
  }

  try {
    await figma.loadFontAsync({ family: "Inter", style: "Regular" });
  } catch (e) {
    /* new text styles default to Inter */
  }

  for (let i = 0; i < entries.length; i++) {
    const e = entries[i];
    const name = styleName(e.platform, e.display);
    await figma.loadFontAsync({ family: e.family, style: e.style });
    let style = byName[name];
    if (!style) {
      style = figma.createTextStyle();
      style.name = name;
    }
    style.fontName = { family: e.family, style: e.style };
    style.fontSize = e.size;
    style.lineHeight = { unit: "PIXELS", value: e.lineHeightPx };
    style.letterSpacing = { unit: "PIXELS", value: 0 };
    style.description = "WeCom " + e.platform.toLowerCase() + " typography token: " + e.slug + ".";
    created.push(name);
  }
  return created;
}

function buildStyleMap(styles) {
  const map = new Map();
  for (const s of styles) map.set(s.name, s);
  return map;
}

function styleIsStrong(name, fontName) {
  if (/strong|bold|强化|强调|特强化/i.test(name)) return true;
  return /bold|semibold|semi bold|medium|heavy|black/i.test((fontName && fontName.style) || "");
}

function isStrongSeg(seg) {
  const w = typeof seg.fontWeight === "number" ? seg.fontWeight : 400;
  const style = (seg.fontName && seg.fontName.style) || "";
  if (w >= 600 || w === 500) return true;
  return /bold|semibold|semi bold|medium|heavy|black/i.test(style);
}

function nodeProfile(node) {
  const segs = node.getStyledTextSegments(["fontSize", "fontName", "fontWeight"]);
  if (!segs.length) return { size: 14, strong: false };
  if (segs.length === 1) {
    return { size: Math.round(segs[0].fontSize), strong: isStrongSeg(segs[0]) };
  }
  let total = 0;
  let wSize = 0;
  let strongChars = 0;
  for (const s of segs) {
    const end = s.end != null ? s.end : 0;
    const start = s.start != null ? s.start : 0;
    const len = Math.max(0, end - start);
    total += len;
    wSize += Math.round(s.fontSize) * len;
    if (isStrongSeg(s)) strongChars += len;
  }
  if (!total) return { size: Math.round(segs[0].fontSize), strong: isStrongSeg(segs[0]) };
  return { size: Math.round(wSize / total), strong: strongChars > total / 2 };
}

function usesMobileContext(node) {
  let p = node.parent;
  while (p) {
    if (/mobile/i.test(p.name)) return true;
    p = p.parent;
  }
  return false;
}

function makeBuckets(styles, prefix, excludePattern) {
  return styles
    .filter((s) => s.name.startsWith(prefix) && !excludePattern.test(s.name))
    .map((s) => ({
      style: s,
      size: Math.round(s.fontSize),
      strong: styleIsStrong(s.name, s.fontName),
    }));
}

function pickStyle(size, strong, buckets) {
  const candidates = buckets.filter((b) => b.strong === strong);
  const list = candidates.length ? candidates : buckets;
  let best = list[0];
  let bestDiff = Infinity;
  for (const b of list) {
    const diff = Math.abs(b.size - size);
    if (diff < bestDiff) {
      bestDiff = diff;
      best = b;
    }
  }
  return best ? best.style : undefined;
}

async function loadNodeFonts(node) {
  const len = node.characters.length;
  if (!len) return;
  const fonts = node.getRangeAllFontNames(0, len);
  for (let i = 0; i < fonts.length; i++) {
    await figma.loadFontAsync(fonts[i]);
  }
}

async function remapTextNodes(allStyles) {
  const exclude = /文章|短文|Message Bubble|Article|Prose/i;
  const desktopBuckets = makeBuckets(allStyles, "WeCom/Desktop Win/", exclude);
  const mobileBuckets = makeBuckets(allStyles, "WeCom/Mobile iOS/", exclude);
  let remapped = 0;

  for (const page of figma.root.children) {
    await figma.setCurrentPageAsync(page);
    const texts = page.findAll((n) => n.type === "TEXT");
    for (const node of texts) {
      const profile = nodeProfile(node);
      const size = profile.size;
      const strong = profile.strong;
      const buckets = usesMobileContext(node) ? mobileBuckets : desktopBuckets;
      const style = pickStyle(size, strong, buckets);
      if (!style) continue;
      await loadNodeFonts(node);
      await figma.loadFontAsync(style.fontName);
      for (const field of ["fontFamily", "fontStyle", "fontSize", "lineHeight", "fontWeight", "letterSpacing"]) {
        try {
          node.setBoundVariable(field, null);
        } catch (e) {
          /* ignore */
        }
      }
      await node.setTextStyleIdAsync(style.id);
      remapped += 1;
    }
  }
  return { remapped };
}

async function deleteOldWecomStyles(keepNames) {
  const keep = new Set(keepNames);
  const all = await figma.getLocalTextStylesAsync();
  const removed = [];
  for (const s of all) {
    if (!s.name.startsWith("WeCom/")) continue;
    if (keep.has(s.name)) continue;
    removed.push(s.name);
    s.remove();
  }
  return removed;
}

async function run() {
  try {
    const entries = flattenEntries();
    const loadErrors = await loadAllFonts(DATA.desktop.concat(DATA.mobile));
    if (loadErrors.length) {
      figma.closePlugin(
        "字体加载失败 " +
          loadErrors.length +
          " 项。请确认 Figma 桌面端已安装 Microsoft YaHei 与 PingFang SC，并重启 FigmaAgent 后重试。\\n" +
          loadErrors
            .slice(0, 3)
            .map(function (item) {
              return item.family + " " + item.style;
            })
            .join(", "),
      );
      return;
    }

    const created = await createStyles(entries);
    const allStyles = await figma.getLocalTextStylesAsync();
    const remap = await remapTextNodes(allStyles);
    const removed = await deleteOldWecomStyles(created);

    figma.closePlugin(
      "完成：新建 " +
        created.length +
        " 个样式，重套用文字 " +
        remap.remapped +
        " 个，清理旧样式 " +
        removed.length +
        " 个。",
    );
  } catch (err) {
    figma.closePlugin("执行失败: " + String(err));
  }
}

run();
`;

const code = template.replace("__DATA__", JSON.stringify(data));
fs.writeFileSync(outPath, code);
console.log("Wrote", outPath, "(" + code.length + " bytes)");
