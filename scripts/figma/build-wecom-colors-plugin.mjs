import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const dataPath = path.join(__dirname, "wecom-color-data.json");
const outPath = path.join(__dirname, "plugin-wecom-sync-colors", "code.js");

const data = JSON.parse(fs.readFileSync(dataPath, "utf8"));

const template = `// Auto-generated by build-wecom-colors-plugin.mjs — do not edit by hand.
var COLOR_DATA = __DATA__;

function findCollection(collections, name) {
  for (var i = 0; i < collections.length; i++) {
    if (collections[i].name === name) return collections[i];
  }
  return null;
}

function rgbaFromEntry(entry) {
  return {
    r: entry.r,
    g: entry.g,
    b: entry.b,
    a: entry.a != null ? entry.a : 1,
  };
}

function near(a, b) {
  return Math.abs(a - b) < 0.004;
}

function colorEqual(a, b) {
  if (!a || !b) return false;
  if (a.type === "VARIABLE_ALIAS") return false;
  if (typeof a.r !== "number" || typeof b.r !== "number") return false;
  return (
    near(a.r, b.r) &&
    near(a.g, b.g) &&
    near(a.b, b.b) &&
    near(a.a != null ? a.a : 1, b.a != null ? b.a : 1)
  );
}

function describeDiff(cur, target) {
  if (!cur || cur.type === "VARIABLE_ALIAS") return "alias";
  function h(n) {
    var s = Math.round(n * 255).toString(16).toUpperCase();
    return s.length === 1 ? "0" + s : s;
  }
  var ca = cur.a != null ? cur.a : 1;
  var ta = target.a != null ? target.a : 1;
  return (
    h(cur.r) +
    h(cur.g) +
    h(cur.b) +
    "@" +
    Math.round(ca * 100) +
    "% -> " +
    h(target.r) +
    h(target.g) +
    h(target.b) +
    "@" +
    Math.round(ta * 100) +
    "%"
  );
}

async function run() {
  try {
    var collections = await figma.variables.getLocalVariableCollectionsAsync();
    var fullColl = findCollection(collections, "WeCom Full Color");
    if (!fullColl) {
      figma.closePlugin("未找到 WeCom Full Color 变量集合");
      return;
    }

    var lightMode = fullColl.modes[0].modeId;
    var darkMode = fullColl.modes.length > 1 ? fullColl.modes[1].modeId : lightMode;
    var allVars = await figma.variables.getLocalVariablesAsync();
    var byName = {};

    for (var i = 0; i < allVars.length; i++) {
      var v = allVars[i];
      if (v.variableCollectionId === fullColl.id && v.resolvedType === "COLOR") {
        byName[v.name] = v;
      }
    }

    var stats = {
      created: 0,
      updated: 0,
      unchanged: 0,
      alphaFixed: 0,
      figmaOnly: 0,
    };
    var samples = [];

    for (var di = 0; di < COLOR_DATA.length; di++) {
      var row = COLOR_DATA[di];
      var targetLight = rgbaFromEntry(row.light);
      var targetDark = rgbaFromEntry(row.dark);
      var variable = byName[row.name];

      if (!variable) {
        variable = figma.variables.createVariable(row.name, fullColl.id, "COLOR");
        byName[row.name] = variable;
        stats.created = stats.created + 1;
      }

      if (row.usage && variable.description !== row.usage) {
        variable.description = row.usage;
      }

      var curLight = variable.valuesByMode[lightMode];
      var curDark = variable.valuesByMode[darkMode];
      var changed = false;

      if (!colorEqual(curLight, targetLight)) {
        variable.setValueForMode(lightMode, targetLight);
        changed = true;
        if (targetLight.a < 1 || (curLight && (curLight.a == null ? 1 : curLight.a) < 1)) {
          stats.alphaFixed = stats.alphaFixed + 1;
        }
        if (samples.length < 5) {
          samples.push(row.name + " Light " + describeDiff(curLight, targetLight));
        }
      }

      if (!colorEqual(curDark, targetDark)) {
        variable.setValueForMode(darkMode, targetDark);
        changed = true;
        if (targetDark.a < 1 || (curDark && (curDark.a == null ? 1 : curDark.a) < 1)) {
          stats.alphaFixed = stats.alphaFixed + 1;
        }
        if (samples.length < 8) {
          samples.push(row.name + " Dark " + describeDiff(curDark, targetDark));
        }
      }

      if (changed) stats.updated = stats.updated + 1;
      else stats.unchanged = stats.unchanged + 1;

      delete byName[row.name];
    }

    var figmaOnlyNames = Object.keys(byName);
    stats.figmaOnly = figmaOnlyNames.length;

    var msg =
      "色彩同步完成：Excel " +
      COLOR_DATA.length +
      " 条，新建 " +
      stats.created +
      "，更新 " +
      stats.updated +
      "，未变 " +
      stats.unchanged +
      "，透明度修正 " +
      stats.alphaFixed +
      "，Figma 多余 " +
      stats.figmaOnly;
    if (samples.length) msg = msg + "。示例：" + samples.join("；");
  if (figmaOnlyNames.length) msg = msg + "。多余：" + figmaOnlyNames.slice(0, 5).join(", ");

    figma.closePlugin(msg);
  } catch (err) {
    figma.closePlugin("执行失败: " + String(err));
  }
}

run();
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(
  outPath,
  template.replace("__DATA__", JSON.stringify(data)),
  "utf8"
);
console.log(`已生成 ${outPath}（${data.length} 条颜色）`);
